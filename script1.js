

function chBgColo0(element) {
    var bg = document.getElementById("list1 tab0");

    bg.style.backgroundColor = "rgb(25, 132, 231)";
    document.getElementById("list1 tab1").style.backgroundColor = "white";
    document.getElementById("list1 tab2").style.backgroundColor = "white";
    document.getElementById("list1 tab3").style.backgroundColor = "white";
    document.getElementById("list1 tab4").style.backgroundColor = "white";

   
    var x = document.getElementById('collapsetxt0');//tab1 text 
    x.style.display ='none';
    if (x.style.display == 'block') {
        x.style.display = 'none';
    } else {
        x.style.display = 'block';
    }


    if (document.getElementById('collapsetxt1').style.display == 'block') { //to close tab1 text
        document.getElementById('collapsetxt1').style.display = 'none';
    } 
    if (document.getElementById('collapsetxt2').style.display == 'block') { //to close tab1 text
        document.getElementById('collapsetxt2').style.display = 'none';
    } 
    if (document.getElementById('collapsetxt3').style.display == 'block') { //to close tab1 text
        document.getElementById('collapsetxt3').style.display = 'none';
    } 
    if (document.getElementById('collapsetxt4').style.display == 'block') { //to close tab1 text
        document.getElementById('collapsetxt4').style.display = 'none';
    } 
    


}




